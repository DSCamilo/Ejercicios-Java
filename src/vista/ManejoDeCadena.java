    private void btn_equalsCase_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_equalsCase_ActionPerformed
        JOptionPane.showMessageDialog(this, "Compara dos strings para ver si "
                + "son iguales ignorando las diferencias entre mayúsculas y "
                + "minúsculas.");

        cadena1 = txt_cadena1.getText();
        cadena2 = txt_cadena2.getText();
        if (cadena1.equalsIgnoreCase(cadena2)) {
            JOptionPane.showMessageDialog(null, "SON IGUALES");
            txt_resultado.setText("SON IGUALES");
        } else {

            JOptionPane.showMessageDialog(null, "NO SON IGUALES");
            txt_resultado.setText("NO SON IGUALES");
        }
    }//GEN-LAST:event_btn_equalsCase_ActionPerformed

