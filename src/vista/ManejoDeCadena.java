    private void btn_indexOfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_indexOfActionPerformed

        cadena1 = txt_cadena1.getText();
        cadena2 = txt_cadena2.getText();

        int posicion = cadena1.indexOf(cadena2);

        if (posicion != -1) {
            JOptionPane.showMessageDialog(null, "Palabra ingresada encontrada");
            txt_resultado.setText("La posicion ingresada de: " + cadena2 + " es " + posicion);
        } else {
            txt_resultado.setText("Palabra no encontrada");
        }


    }//GEN-LAST:event_btn_indexOfActionPerformed

